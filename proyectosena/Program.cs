using Microsoft.AspNetCore.Authentication.JwtBearer;
using Microsoft.EntityFrameworkCore;
using Microsoft.IdentityModel.Tokens;
using Microsoft.OpenApi;
using proyectosena.Context;
using System.Text;

var builder = WebApplication.CreateBuilder(args);

// ??????????????????????????????????????????????
// 1. LOGGING
// ??????????????????????????????????????????????
builder.Logging.ClearProviders();
builder.Logging.AddConsole();
builder.Logging.AddDebug();

// ??????????????????????????????????????????????
// 2. BASE DE DATOS
// ??????????????????????????????????????????????
builder.Services.AddDbContext<RecyRouteDbContext>(options =>
    options.UseSqlServer(
        builder.Configuration.GetConnectionString("RecyRouteConnection"),
        sqlOptions => sqlOptions.EnableRetryOnFailure(
            maxRetryCount: 3,
            maxRetryDelay: TimeSpan.FromSeconds(5),
            errorNumbersToAdd: null
        )
    )
);

// ??????????????????????????????????????????????
// 3. JWT AUTENTICACIÓN
// ??????????????????????????????????????????????
builder.Services.AddAuthentication(JwtBearerDefaults.AuthenticationScheme)
    .AddJwtBearer(options =>
    {
        options.TokenValidationParameters = new TokenValidationParameters
        {
            ValidateIssuer = true,
            ValidateAudience = true,
            ValidateLifetime = true,
            ValidateIssuerSigningKey = true,
            ValidIssuer = builder.Configuration["Jwt:Issuer"],
            ValidAudience = builder.Configuration["Jwt:Audience"],
            IssuerSigningKey = new SymmetricSecurityKey(
                                           Encoding.UTF8.GetBytes(
                                               builder.Configuration["Jwt:Key"]!)),
            ClockSkew = TimeSpan.Zero
        };
    });

// ??????????????????????????????????????????????
// 4. AUTORIZACIÓN POR ROLES
// ??????????????????????????????????????????????
builder.Services.AddAuthorization(options =>
{
    options.AddPolicy("AdminOnly", policy => policy.RequireRole("Administrador"));
    options.AddPolicy("GestorOnly", policy => policy.RequireRole("Gestor"));
    options.AddPolicy("CiudadanoOnly", policy => policy.RequireRole("Ciudadano"));
    options.AddPolicy("AdminOrGestor", policy => policy.RequireRole("Administrador", "Gestor"));
});

// ??????????????????????????????????????????????
// 5. CORS
// ??????????????????????????????????????????????
builder.Services.AddCors(options =>
{
    options.AddPolicy("RecyRoutePolicy", policy =>
    {
        policy.AllowAnyOrigin()
              .AllowAnyMethod()
              .AllowAnyHeader();
    });
});

// ??????????????????????????????????????????????
// 6. SWAGGER — Swashbuckle 10.x + OpenApi v2
// ??????????????????????????????????????????????
builder.Services.AddEndpointsApiExplorer();
builder.Services.AddSwaggerGen(options =>
{
    options.SwaggerDoc("v1", new OpenApiInfo
    {
        Title = "RecyRoute API",
        Version = "v1",
        Description = "API para la gestión de solicitudes de recolección de residuos"
    });

    // ? Swashbuckle 10.x: definición del esquema Bearer
    options.AddSecurityDefinition("Bearer", new OpenApiSecurityScheme
    {
        Description = @"JWT Authorization header using the Bearer scheme. <br/><br/>
                         Escribe: Bearer [space] y luego tu token. <br/><br/>
                         Ejemplo: 'Bearer abc123xyz'",
        Name = "Authorization",
        In = ParameterLocation.Header,
        Type = SecuritySchemeType.ApiKey,
        Scheme = "Bearer"
    });

    // ? Swashbuckle 10.x: AddSecurityRequirement recibe un lambda (doc =>)
    //    y la key es OpenApiSecuritySchemeReference en lugar de OpenApiSecurityScheme
    options.AddSecurityRequirement(doc => new OpenApiSecurityRequirement
    {
        {
            new OpenApiSecuritySchemeReference("Bearer"),
            new List<string>()
        }
    });
});

// ??????????????????????????????????????????????
// 7. SERVICIOS (descomentar cuando se creen)
// ??????????????????????????????????????????????
// builder.Services.AddScoped<IAuthService, AuthService>();
// builder.Services.AddScoped<IUserService, UserService>();
// builder.Services.AddScoped<ICollectionRequestService, CollectionRequestService>();
// builder.Services.AddScoped<ICollectionManagementService, CollectionManagementService>();
// builder.Services.AddScoped<INotificationService, NotificationService>();
// builder.Services.AddScoped<IChatHistoryService, ChatHistoryService>();

builder.Services.AddControllers();

// ??????????????????????????????????????????????
// BUILD
// ??????????????????????????????????????????????
var app = builder.Build();

// ??????????????????????????????????????????????
// 8. PIPELINE DE MIDDLEWARES
// ??????????????????????????????????????????????
if (app.Environment.IsDevelopment())
{
    app.UseSwagger();
    app.UseSwaggerUI(options =>
    {
        options.SwaggerEndpoint("/swagger/v1/swagger.json", "RecyRoute API v1");
        options.RoutePrefix = string.Empty; // Swagger en la raíz
    });
}

app.UseHttpsRedirection();
app.UseCors("RecyRoutePolicy");
app.UseAuthentication(); // ?? Siempre antes de UseAuthorization
app.UseAuthorization();

// ?? Middleware 401 personalizado ???????????????
app.Use(async (context, next) =>
{
    await next();
    if (context.Response.StatusCode == StatusCodes.Status401Unauthorized)
    {
        context.Response.ContentType = "application/json";
        var result = System.Text.Json.JsonSerializer.Serialize(new
        {
            mensaje = "Acceso no autorizado. Verifique su token o credenciales."
        });
        await context.Response.WriteAsync(result);
    }
});

app.MapControllers();
app.Run();